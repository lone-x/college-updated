                        <div class="card-body">
                            <p><strong>(a) Academic:</strong></p>
                            <p>Candidates who have passed Higher Secondary Examination, Kerala, or Examinations recognized as equivalent thereto, with Physics and Mathematics as compulsory subjects and Chemistry as one of the optional subjects with at least 45% marks put together in the above subjects are eligible for admission. In case, the candidate has not studied Chemistry, the marks obtained in Computer Science shall be considered. In case, the candidate has not studied Chemistry and Computer Science the marks obtained in Biotechnology shall be considered. In case, the candidate has not studied Chemistry, Computer Science and Biotechnology the marks obtained in Biology shall be considered. The marks as shown in the mark list of the Board of Examination obtained from the respective Higher Secondary Board shall be considered for academic eligibility.</p>
                            <ul>
                                <li>i) In two year Kerala Higher Secondary courses or examinations recognised equivalent thereto with two year course where the Board Examinations are conducted in both years, the total marks of two years in the respective subjects as shown in the mark lists of the respective Higher Secondary Boards will be considered for academic eligibility.</li>
                                <li>ii) In Higher Secondary courses or examinations recognised equivalent thereto where Board Examinations are conducted only at the end of 12th class (final year), the marks in the respective subjects as shown in the mark lists of the respective Higher Secondary Boards will be considered for academic eligibility.</li>
                                <li>iii) For all other type of Higher Secondary courses or examinations recognised equivalent thereto, the marks of the respective subjects as shown in the mark list of the respective Board of Examinations will be considered for academic eligibility.</li>
                                <li>iv) The Vocational Higher Secondary Examination, Kerala, has been recognised as equivalent to the Higher Secondary Examination, Kerala.</li>
                                <li>v) Candidates who have passed their qualifying examination from outside the State of Kerala, shall produce a certificate of recognition and equivalency of qualification at the time of the personal interview.</li>
                            </ul>
                            <p><strong>Relaxation in marks:</strong></p>
                            <p>In the case of Engineering Courses, candidates belonging to SC/ST/ SEBC or PwD the minimum mark obtained in Physics, Mathematics, Chemistry/Computer Science/Biotechnology/Biology taken together in the qualifying examination shall be 40% instead of 45%.</p>

                            <p><strong>(b) Lateral Entry:</strong></p>
                            <ul>
                                <li>i) For Lateral Entry, candidates shall have passed Diploma in Engineering awarded by the State Board of Technical Education or equivalent after undergoing an institutional course of at least 3 years and must have minimum of 60% marks for the Diploma. For candidates belonging to SC/ST community the minimum is 50% for the diploma.</li>
                                <li>ii) Lateral Entry will be permitted only to the branch of Engineering in which they have obtained the Diploma or corresponding branches.</li>
                                <li>iii) Eligibility for admission is as prescribed by the Commissioner for Entrance examinations, Govt. of Kerala.</li>
                            </ul>

                            <p><strong>(c) Age:</strong></p>
                            <ul>
                                <li>i) The applicant should have completed 17 years of age as on 31st December 2023. No relaxation in the minimum age will be allowed. There is no upper age limit.</li>
                                <li>ii) For lateral Entry maximum age as on last date of submission of application will be 25 years.</li>
                            </ul>
